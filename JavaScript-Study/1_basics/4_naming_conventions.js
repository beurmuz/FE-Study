/**
 * Naming Conventions
 *
 * 변수 이름 지을때
 * 1) 일반적으로 영어(라틴문자)를 사용하며 문자와 숫자를 모두 사용 할 수 있다.
 * 2) 특수기호는 언더스코어'_'와 달러'$'를 사용 할 수 있다.
 * 3) 숫자로 이름을 시작 할 수 없다.
 *    1Name, 2Hello
 * 4) 키워드는 변수명으로 사용 할 수 없다.
 *    var const = 'var';
 */
let name = "beurmuz";
var $name = "beurmuz";
const _names = "beurmuz";

console.log(name);
console.log($name);
console.log(_names);

